class Firstclass{
    public static void main(String args[]){
        System.out.print("Hello World :)");//no next line
        System.out.println("Hello World :)");//nextline
        System.out.println("Hello World :)");//next line
        System.out.println("Hello World :)");
        System.out.print("Hello World :)\n");//Also next line
        System.out.println("Hello World :)\nHello\nSahaja");
    }
}